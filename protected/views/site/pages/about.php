<h1>Reseña</h1>

<p>
Agrupación de músicos legado nace el año 2014 con el fin de  fomentar e incentivar la expresión y desarrollo de todos los artistas de la comuna. 
La iniciativa de Benjamín Cataldo, su primer presidente, reúne hoy a más de 120 miembros de diferentes edades y variados géneros musicales de la comuna de Parral, contando, entre otras cosas, con un una sala de ensayo comunitaria 100% equipada.
Actualmente, bajo la dirección de Franco Maldonado, LEGADO es parte de la producción de varios eventos comunales como Legado Festival (Festival donde se presentan todas las bandas de la agrupación, y bandas externas a la comuna), Legado en concierto (concierto anual con bandas de la agrupación en el Teatro Municipal de Parral), Rock a la escuela (encuentro de bandas escolares), Sembrando Cultura (feria artesanal con tres escenarios de música folclórica, clásica y popular), Neruda rock (musicalización de poemas de Neruda) y el concepto Tocatas a tu pinta que busca cultivar las capacidades de producción de eventos en todos los integrantes de la agrupación dando la oportunidad de que mensualmente, una banda se encargue de organizar una actividad de forma autónoma y de acuerdo a sus preferencias. 
De esta forma, se pretende valorizar al máximo,  al músico local, brindándoles herramientas para apoyarlos  en su desarrollo y crecimiento artístico. 
